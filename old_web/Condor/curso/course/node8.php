<h1 ><a name="SECTION00080000000000000000">Last remarks</a></h1>

<p>While we have covered most of what you would normally use with Condor, there are
also some other functionalities worth exploring:</p>

<ul>
<li>The Java Universe, which is better suited to run Java applications. More
  info at <br />
  http://www.cs.wisc.edu/condor/manual/v6.6/2_8Java_Applications.html</li>
<li>The possibility of creating DAGs within DAGs for really complex job
  dependencies. See section 2.11.9 in <br />
  http://research.iac.es/sieinvens/SINFIN/Condor/v6.6/2_11DAGMan_Applications.html</li>
<li>If you need more power for your job dependencies, you can use the Perl
  module described 
  in<br /> 
  http://www.cs.wisc.edu/condor/manual/v6.6/4_4Condor_Perl.html. <br />
  With it you
  can create very versatile dependencies, including conditional branches, cycles, etc.</li>
</ul>

<p>
Condor is a project in constant development and many other interesting research
areas are being pursued. If you would like to find out about all this, check
its official webpage at <a name="tex2html10"
  href="http://www.cs.wisc.edu/condor/">http://www.cs.wisc.edu/condor/</a>
</p>

<p>Enjoy Condor!</p>




