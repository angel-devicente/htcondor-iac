<h1><a name="SECTION00020000000000000000" id="SECTION00020000000000000000">
&#191;Qu&#233; es Condor?</a>
</h1>

<p>
Condor es un proyecto de la Universidad de Wisconsin-Madison (UW-Madison). Est&#225;
ideado para aprovechar al m&#225;ximo la capacidad computacional de una red de ordenadores.
Normalmente s&#243;lo disponemos de la potencia del ordenador que estamos usando para ejecutar
nuestros trabajos, y si, por ejemplo, tuvi&#233;ramos que lanzar 100 veces un mismo programa
con distinta entrada, tendr&#237;amos que hacerlo secuencialmente con la consecuente p&#233;rdida de
tiempo. Condor nos permite ejecutar nuestro trabajo en tantas m&#225;quinas como
haya disponibles, por lo que, en el mejor de los casos, nuestro trabajo
finalizar&#225; en el tiempo que tarda en ejecutarse el m&#225;s lento de nuestros
procesos.</p>
<p>
Condor pone a nuestra disposici&#243;n toda la capacidad de c&#225;lculo desaprovechada en nuestra
red, de esta manera, los recursos disponibles se incrementan considerablemente.</p>
<p>
Condor nos ser&#225; &#250;til siempre que necesitemos ejecutar un trabajo intenso, tanto 
computacionalmente como en el tiempo. Al aprovechar solamente recursos ociosos no
influye en el uso cotidiano de los ordenadores (ver sec. <a href="node3.php#sec::how_it_work">2</a>).</p>
<p>
Adem&#225;s, nos permite:</p>
<ul>
<li>Conocer el estado de nuestros trabajos en cada momento
</li>
<li>Implementar nuestras propias pol&#237;ticas de orden de ejecuci&#243;n
</li>
<li>Mantener un registro de la actividad de nuestros trabajos
</li>
<li>A&#241;adir tolerancia a fallos a nuestros trabajos
</li>
</ul>

